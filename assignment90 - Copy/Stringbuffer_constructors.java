package assignment90;

public class Stringbuffer_constructors {
	public static void main(String[]args)
	{
		StringBuffer s1=new StringBuffer("r");
		System.out.println(s1);
		StringBuffer s2=new StringBuffer("rashmi");
		System.out.println(s2);
		StringBuffer s3=new StringBuffer("narayanaswamy");
		System.out.println(s3.capacity());
		
		//StringBuilder_constructors
		StringBuilder s4=new StringBuilder("r");
		System.out.println(s4);
		StringBuilder s5=new StringBuilder("rashmi");
		System.out.println(s5);
		StringBuilder s6=new StringBuilder("narayanaswamy");
		System.out.println(s6.capacity());
		
	}

}
